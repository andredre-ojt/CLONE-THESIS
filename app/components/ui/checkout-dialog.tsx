"use client"

import { useState, useEffect } from "react"
import { CreditCard, Banknote, CheckCircle2, X } from "lucide-react"

interface CartItem {
    id: string
    name: string
    price: number
    quantity: number
    category: string
    image: string
}

interface CheckoutDialogProps {
    open: boolean
    onClose: () => void
    onComplete: () => void
    total: number
    items: CartItem[]
    onAddTransaction: (transaction: any) => void
}

export function CheckoutDialog({ open, onClose, onComplete, total, items, onAddTransaction }: CheckoutDialogProps) {
    const [paymentMethod, setPaymentMethod] = useState<"card" | "cash">("card")
    const [cashReceived, setCashReceived] = useState("")
    const [processing, setProcessing] = useState(false)
    const [success, setSuccess] = useState(false)
    const [waitingForCard, setWaitingForCard] = useState(false)

    const getCashAmount = () => {
        return Number.parseFloat(cashReceived || "0")
    }

    const calculateChange = () => {
        if (paymentMethod !== "cash") return 0
        const cash = getCashAmount()
        return Math.max(0, cash - total)
    }

    const isSufficientCash = () => {
        return getCashAmount() >= total
    }

    const change = calculateChange()

    const handleComplete = async () => {
        if (paymentMethod === "card") {
            // Show RFID tap modal for card payments
            setWaitingForCard(true)
            return
        }

        // For cash payments, process immediately
        setProcessing(true)

        // Simulate payment processing
        await new Promise((resolve) => setTimeout(resolve, 1500))

        // Add transaction to history
        onAddTransaction({
            items: items.map((item) => ({
                name: item.name,
                quantity: item.quantity,
                price: item.price,
            })),
            total,
            paymentMethod,
            cashReceived: paymentMethod === "cash" ? getCashAmount() : undefined,
            change: paymentMethod === "cash" ? change : 0,
        })

        setSuccess(true)

        // Reset and close after showing success
        setTimeout(() => {
            setProcessing(false)
            setSuccess(false)
            setCashReceived("")
            setPaymentMethod("card")
            onComplete()
        }, 2000)
    }

    const handleCardTap = async () => {
        setWaitingForCard(false)
        setProcessing(true)

        // Simulate card processing
        await new Promise((resolve) => setTimeout(resolve, 1500))

        // Add transaction to history
        onAddTransaction({
            items: items.map((item) => ({
                name: item.name,
                quantity: item.quantity,
                price: item.price,
            })),
            total,
            paymentMethod: "card",
            cashReceived: undefined,
            change: 0,
        })

        setSuccess(true)

        // Reset and close after showing success
        setTimeout(() => {
            setProcessing(false)
            setSuccess(false)
            setCashReceived("")
            setPaymentMethod("card")
            onComplete()
        }, 2000)
    }

    const handleClose = () => {
        if (!processing && !success && !waitingForCard) {
            setCashReceived("")
            setPaymentMethod("card")
            onClose()
        }
    }

    // Add event listener for key press (RFID tap simulation)
    useEffect(() => {
        if (waitingForCard) {
            const handleKeyPress = (e: KeyboardEvent) => {
                e.preventDefault()
                handleCardTap()
            }
            window.addEventListener("keydown", handleKeyPress)
            return () => window.removeEventListener("keydown", handleKeyPress)
        }
    }, [waitingForCard])

    if (!open) return null

    return (
        <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/50">
            <div className="relative w-full max-w-md rounded-lg bg-white p-6 shadow-lg">
                <button
                    onClick={handleClose}
                    className="absolute right-4 top-4 rounded-sm opacity-70 hover:opacity-100 disabled:pointer-events-none"
                    disabled={processing || success || waitingForCard}
                >
                    <X className="h-4 w-4" />
                </button>

                <div className="mb-6">
                    <h2 className="text-xl font-semibold">
                        {waitingForCard ? "Tap Your Card" : "Complete Payment"}
                    </h2>
                    <p className="text-sm text-gray-500">
                        {waitingForCard
                            ? "Please tap your RFID card on the reader"
                            : "Select payment method and complete the transaction"}
                    </p>
                </div>

                {waitingForCard ? (
                    <div className="flex flex-col items-center justify-center py-8">
                        <div className="mb-6 flex h-24 w-24 items-center justify-center rounded-full bg-blue-100 animate-pulse">
                            <CreditCard className="h-12 w-12 text-blue-600" />
                        </div>
                        <h3 className="mb-2 text-xl font-semibold">Waiting for Card</h3>
                        <p className="text-center text-gray-500 mb-4">Tap your RFID card to complete payment</p>
                        <div className="rounded-lg border bg-gray-50 p-4 w-full">
                            <div className="flex justify-between">
                                <span className="text-lg font-medium">Amount to Pay</span>
                                <span className="text-2xl font-bold text-blue-600">₱{total.toFixed(2)}</span>
                            </div>
                        </div>
                        <button
                            onClick={() => setWaitingForCard(false)}
                            className="mt-4 text-sm text-gray-600 hover:text-gray-800 underline"
                        >
                            Cancel
                        </button>
                    </div>
                ) : success ? (
                    <div className="flex flex-col items-center justify-center py-8">
                        <div className="mb-4 flex h-16 w-16 items-center justify-center rounded-full bg-green-100">
                            <CheckCircle2 className="h-8 w-8 text-green-600" />
                        </div>
                        <h3 className="mb-2 text-xl font-semibold">Payment Successful!</h3>
                        <p className="text-gray-500">Transaction completed</p>
                    </div>
                ) : (
                    <div className="space-y-6">
                        <div>
                            <label className="mb-3 block text-sm font-medium">Payment Method</label>
                            <div className="grid grid-cols-2 gap-3">
                                <button
                                    className={`flex h-20 flex-col items-center justify-center gap-2 rounded-lg border-2 transition-colors ${paymentMethod === "card"
                                            ? "border-black bg-black text-white"
                                            : "border-gray-300 bg-white hover:bg-gray-50"
                                        }`}
                                    onClick={() => setPaymentMethod("card")}
                                >
                                    <CreditCard className="h-6 w-6" />
                                    <span>Card</span>
                                </button>
                                <button
                                    className={`flex h-20 flex-col items-center justify-center gap-2 rounded-lg border-2 transition-colors ${paymentMethod === "cash"
                                            ? "border-black bg-black text-white"
                                            : "border-gray-300 bg-white hover:bg-gray-50"
                                        }`}
                                    onClick={() => setPaymentMethod("cash")}
                                >
                                    <Banknote className="h-6 w-6" />
                                    <span>Cash</span>
                                </button>
                            </div>
                        </div>

                        {paymentMethod === "cash" && (
                            <div className="space-y-4">
                                <div>
                                    <label htmlFor="cash-received" className="block text-sm font-medium mb-1.5">
                                        Cash Received
                                    </label>
                                    <input
                                        id="cash-received"
                                        type="number"
                                        step="0.01"
                                        placeholder="0.00"
                                        value={cashReceived}
                                        onChange={(e) => setCashReceived(e.target.value)}
                                        className="w-full rounded-lg border border-gray-300 px-4 py-2 focus:outline-none focus:ring-2 focus:ring-black"
                                    />
                                </div>
                                {cashReceived && (
                                    <div
                                        className={`rounded-lg border p-3 ${isSufficientCash()
                                                ? "border-green-200 bg-green-50"
                                                : "border-red-200 bg-red-50"
                                            }`}
                                    >
                                        {isSufficientCash() ? (
                                            <div className="flex justify-between">
                                                <span className="font-semibold text-green-700">Change Due</span>
                                                <span className="text-xl font-bold text-green-700">
                                                    ₱{change.toFixed(2)}
                                                </span>
                                            </div>
                                        ) : (
                                            <div className="flex items-center justify-between">
                                                <span className="text-sm text-red-700">Insufficient amount</span>
                                                <span className="text-sm font-medium text-red-700">
                                                    Need ₱{(total - getCashAmount()).toFixed(2)} more
                                                </span>
                                            </div>
                                        )}
                                    </div>
                                )}
                            </div>
                        )}

                        <div className="rounded-lg border bg-gray-50 p-4">
                            <div className="flex justify-between">
                                <span className="text-lg font-medium">Total Amount</span>
                                <span className="text-2xl font-bold text-blue-600">₱{total.toFixed(2)}</span>
                            </div>
                        </div>

                        <button
                            className={`w-full rounded-lg px-4 py-3 text-lg font-semibold text-white transition-colors ${processing || (paymentMethod === "cash" && !isSufficientCash())
                                    ? "cursor-not-allowed bg-gray-400"
                                    : "bg-black hover:bg-gray-800"
                                }`}
                            onClick={handleComplete}
                            disabled={processing || (paymentMethod === "cash" && !isSufficientCash())}
                        >
                            {processing ? "Processing..." : "Complete Payment"}
                        </button>
                    </div>
                )}
            </div>
        </div>
    )
}

export default CheckoutDialog